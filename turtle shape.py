import turtle as t
t.shape("turtle")
t.color("yellow")
t.begin_fill()
t.forward(100)
t.right(120)
t.forward(100)
t.right(120)
t.forward(100)
t.end_fill()
t.penup()
t.forward(200)
t.pendown()
t.right(75)
t.forward(50)
t.right(90)
t.forward(50)
t.right(90)
t.forward(50)
t.right(90)
t.forward(50)
t.penup()
t.forward(100)
t.pendown()
t.color("red")
t.begin_fill()
t.circle(20,steps=6)#hexagon
t.end_fill()
t.penup()
t.backward(100)
t.pendown()
t.color("green")
t.begin_fill()
t.speed(2)
t.circle(20,steps=5)
t.end_fill()
t.done()
